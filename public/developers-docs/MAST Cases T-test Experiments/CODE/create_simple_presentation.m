               '• ADC PZ thresholds → ADC-based features (ADC_HRS1_Mean, ADC_HRS6_Mean)'];
        
        % Slide 4: Analysis Results
        slide4 = presentation.Slides.Add(4, 2);
        slide4.Shapes.Title.TextFrame.TextRange.Text = 'Analysis Results Overview';
        slide4.Shapes.Item(2).TextFrame.TextRange.Text = ['✅ ADC_Prostate_Mean: 100% identical (EXPECTED - not affected by parameters)' char(10) char(10)...
               '✅ HRS1_Volume: 68-94% identical (GOOD - shows variation)' char(10)...
               '✅ HRS6_Volume: 62-92% identical (GOOD - shows variation)' char(10)...
               '✅ HRS7_6_Ratio: 60-91% identical (GOOD - shows variation)' char(10)...
               '✅ ADC_HRS1_Mean: 57-88% identical (GOOD - shows variation)' char(10)...
               '✅ ADC_HRS6_Mean: 51-81% identical (GOOD - shows variation)' char(10) char(10)...
