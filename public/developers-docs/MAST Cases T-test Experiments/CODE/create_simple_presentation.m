        slide7 = presentation.Slides.Add(7, 2);
        slide7.Shapes.Title.TextFrame.TextRange.Text = 'Conclusions';
        slide7.Shapes.Item(2).TextFrame.TextRange.Text = ['✅ Parameter variations are working correctly' char(10) char(10)...
